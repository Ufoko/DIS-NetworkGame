package server.Model.Objects;

public class Chest {
    private  Pair location;
    private   int point;

    public Chest(Pair location, int point) {
        this.location = location;
        this.point = point;
    }


    public int getXpos() {
        return getXpos();
    }

    public int getYPos(){
        return getYPos();
    }


    public int getPoint() {
        return point;
    }
}
